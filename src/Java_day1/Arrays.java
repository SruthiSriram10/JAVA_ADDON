package Java_day1;

public class Arrays {
	public static void main(String[] args) {
		int []num=new int[5]; //single dimensional array
		num[0]=10;
		num[1]=20;
		num[2]=30;
		num[3]=40;
		num[4]=50;
		System.out.println(num[0]+"\n");
		for (int i=0;i<num.length;i++) {
			System.out.println(num[i]);
			}
		}
	}
